public class MapGeneratorEditor : Editor
{
    public override void OnInspectorGUI()
    {
        DrawDefaultInspector();
        MapGenerator myScript = (MapGenerator)target;
        if (GUILayout.Button("Generate New Map"))
        {
            myScript.GenerateMap();
        }
    }
}
